 //Вкладені цикли 

 /*
Вивести на консоль таблицю множення від 1 до 5

1*1 = 1
1*2 = 2
...1*5 = 5

2*1 = 2
......
...5*5 = 25

 */

for (let i = 1; i<=5; i++) { // число , для якого ми виводимо таблицю множення
    
    for (let j = 1; j<=5; j++) { //внутрішній цикл 1*1, 1*2, 1*3, 1*4, 1*5 (ми слідкуємо за числами на які ми множимо число з зовнішнього циклу)
        console.log(`${i} * ${j} = ${i*j}`);
    }  
}

