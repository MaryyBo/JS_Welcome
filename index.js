  //Задача: Збільшити значення  k на 3

  k = k + 3; // 3

  // Задача: Зменшити значення k на 2

  k = k - 2;  // -2

  //Можна скоротити цю роботу.
  //Задача: Збільшити значення  k на 3

   k += 3; // те саме, що й : k = k + 3
   k -= 2; // те саме, що й : k = k - 2
   k *= 4; // те саме, що й : k = k * 4
