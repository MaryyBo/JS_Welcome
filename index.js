// Increment (збільшення), decrement(зменшення)

let superNumber = 1;

//Задача: збільшити значення змінної superNumber на 1.

superNumber++; // 1 + 1 = 2
//Значення збільшується на 1

// Increment - Операція, яка використ для збільшення числа на 1
// Decrement - Операція, яка використ для зменшення числа на 1

superNumber--; // 1 - 1 = 0
 //Значення зменшується на 1

 //-------------------------------------------

 //  Prefix
 ++superNumber; // Iнкремент буде виконано першим ділом, потім всі інші операції
 --superNumber; // Декремент буде виконано першим ділом, потім всі інші операції
 

 //  Postfix
 //  Postfix

  superNumber++;// Спочатку буде використано значення змінної, а після завершення операції мінна інкрементується
  superNumber--; // Спочатку буде використано значення змінної, а після завершення операції змінна декрементується

  console.log(++superNumber); // 1 + 1 = 2

  //------------------------------------------

  //Збільшення НЕ на 1 (> 1)

